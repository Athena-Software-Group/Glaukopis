#!/usr/bin/env python3
"""Build SFT/test/benchmark_data/athena_bench/athena_taa_canonical/
athena-cti-taa-canonical.jsonl from MITRE ATT&CK + Athena aliases.csv.

This is the held-out evaluation slice paired with the TAA.CANON.{1,2,3}
training family generated by tmpl_gen/scripts/taa_canon_generator.py
(see tmpl_gen/templates/05052026/v12_plan.txt §10).

The eval task: given a single vendor-specific alias for an intrusion
set, the model must emit the canonical MITRE ATT&CK group name and
G-code. One alias per group; deterministic per --seed; size capped by
--num-rows. Eligible groups have at least 2 distinct aliases (so the
prompt alias is a non-canonical name, not the canonical one).

Output schema mirrors athena-cti-taa.jsonl plus traceability fields:
    prompt:           the question to send the model
    answer:           the canonical group name (the strict-match target)
    canonical_name:   redundant copy of answer (kept for parity)
    mitre_id:         the G-code (G####)
    alias_used:       the vendor alias the prompt asks about
    prompt_hash:      sha256(prompt)[:64]
"""

from __future__ import annotations

import argparse
import csv
import hashlib
import json
import random
import sys
from collections import defaultdict
from pathlib import Path


PROMPT_TEMPLATES: list[str] = [
    'Resolve the vendor-specific threat-actor alias "{alias}" to its '
    'canonical MITRE ATT&CK intrusion-set name and G-code. Consult the '
    'MITRE ATT&CK groups knowledge base aliases catalog and report the '
    'canonical group name.\n\nThe final line of your response must begin '
    'with "Answer:" and contain only the canonical MITRE ATT&CK group '
    'name.\n\nAlias: {alias}\n',
    'Vendor reporting refers to a single intrusion set as "{alias}". '
    'What is the canonical MITRE ATT&CK group name and identifier for '
    'this alias?\n\nThe final line of your response must begin with '
    '"Answer:" and contain only the canonical MITRE ATT&CK group '
    'name.\n\nAlias: {alias}\n',
    'Map the vendor-naming alias "{alias}" to the canonical MITRE '
    'ATT&CK intrusion-set name and G-code per the MITRE ATT&CK groups '
    'knowledge base.\n\nThe final line of your response must begin '
    'with "Answer:" and contain only the canonical MITRE ATT&CK group '
    'name.\n\nAlias: {alias}\n',
]


def load_mitre_groups(path: Path) -> list[dict]:
    bundle = json.loads(path.read_text())
    return [o for o in bundle["objects"]
            if o.get("type") == "intrusion-set"
            and not o.get("revoked") and not o.get("x_mitre_deprecated")]


def mitre_id(group: dict) -> str | None:
    for er in group.get("external_references", []):
        if er.get("source_name") == "mitre-attack":
            return er.get("external_id")
    return None


def load_athena_aliases(path: Path) -> dict[str, set[str]]:
    by_actor: dict[str, set[str]] = defaultdict(set)
    with path.open() as f:
        for row in csv.DictReader(f):
            actor = (row.get("ThreatActor") or "").strip()
            alias = (row.get("Alias") or "").strip()
            if actor and alias:
                by_actor[actor].add(alias)
    return by_actor


def merged_aliases(group: dict, athena: dict[str, set[str]]) -> list[str]:
    aliases: list[str] = list(group.get("aliases") or [])
    name = group.get("name", "")
    pool: set[str] = set()
    if name in athena:
        pool.update(athena[name])
    for a in aliases:
        if a in athena:
            pool.update(athena[a])
    seen: set[str] = set()
    out: list[str] = []
    for a in aliases + sorted(pool):
        norm = a.strip()
        if norm and norm.lower() not in seen:
            seen.add(norm.lower())
            out.append(norm)
    return out


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawTextHelpFormatter)
    p.add_argument("--mitre", type=Path,
                   default=Path("cpt/cache/raw/mitre_attack_enterprise/enterprise-attack.json"))
    p.add_argument("--athena-aliases", type=Path,
                   default=Path("SFT/test/benchmark_data/athena_bench/athena_taa/aliases.csv"))
    p.add_argument("--output", type=Path,
                   default=Path("SFT/test/benchmark_data/athena_bench/athena_taa_canonical/athena-cti-taa-canonical.jsonl"))
    p.add_argument("--num-rows", type=int, default=500)
    p.add_argument("--seed", type=int, default=42)
    args = p.parse_args()

    rng = random.Random(args.seed)
    groups = load_mitre_groups(args.mitre)
    athena = load_athena_aliases(args.athena_aliases)

    eligible: list[tuple[dict, str, list[str]]] = []
    for g in groups:
        gid = mitre_id(g)
        canonical = (g.get("name") or "").strip()
        if not gid or not canonical:
            continue
        aliases = merged_aliases(g, athena)
        non_canonical = [a for a in aliases if a.strip().lower() != canonical.lower()]
        if non_canonical:
            eligible.append((g, canonical, non_canonical))

    rng.shuffle(eligible)
    rows: list[dict] = []
    for g, canonical, non_canonical in eligible[:args.num_rows]:
        alias = rng.choice(non_canonical)
        prompt = rng.choice(PROMPT_TEMPLATES).format(alias=alias)
        rows.append({
            "prompt": prompt,
            "answer": canonical,
            "canonical_name": canonical,
            "mitre_id": mitre_id(g),
            "alias_used": alias,
            "prompt_hash": hashlib.sha256(prompt.encode()).hexdigest(),
        })

    args.output.parent.mkdir(parents=True, exist_ok=True)
    with args.output.open("w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"wrote {len(rows):,} rows to {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
